<?php

require_once "conexion.php";

class Condiciones
{

    /*=============================================
	MOSTRAR USUARIOS
	=============================================*/

    static public function mdlMostrarUsuarios($item)
    {

        // if ($item != null) {

        $stmt = Conexion::conectar()->prepare("SELECT id_sucursal FROM empleados WHERE id = :id");

        $stmt->bindParam(":id", $item, PDO::PARAM_STR);

        $stmt->execute();

        return $stmt->fetch();

        $stmt->close();

        $stmt = null;
    }


    /*=============================================
	REGISTRO DE USUARIO
	=============================================*/

    static public function mdlIngresarUsuario($tabla, $datos)
    {

        $stmt = Conexion::conectar()->prepare("INSERT INTO $tabla(id_empleado, id_rol, usuario, password, foto) VALUES (:id_empleado, :id_rol, :usuario, :password, :foto)");


        $stmt->bindParam(":id_empleado", $datos["id_empleado"], PDO::PARAM_STR);
        $stmt->bindParam(":id_rol", $datos["id_rol"], PDO::PARAM_STR);
        $stmt->bindParam(":usuario", $datos["usuario"], PDO::PARAM_STR);
        $stmt->bindParam(":password", $datos["password"], PDO::PARAM_STR);
        $stmt->bindParam(":foto", $datos["foto"], PDO::PARAM_STR);

        if ($stmt->execute()) {

            return "ok";
        } else {

            return "error";
        }

        $stmt->close();

        $stmt = null;
    }


    /*=============================================
	ACTUALIZAR USUARIO
	=============================================*/

    static public function mdlActualizarUsuario($tabla, $item1, $valor1, $item2, $valor2)
    { //este modelo sirve solo para registrar la fecha de entrada y estatus del usuario

        $stmt = Conexion::conectar()->prepare("UPDATE $tabla SET $item1 = :$item1 WHERE $item2 = :$item2");

        $stmt->bindParam(":" . $item1, $valor1, PDO::PARAM_STR);
        $stmt->bindParam(":" . $item2, $valor2, PDO::PARAM_STR);

        if ($stmt->execute()) {

            return "ok";
        } else {

            return "error";
        }

        $stmt->close();

        $stmt = null;
    }


    /*=============================================
	BORRAR USUARIO
	=============================================*/

    static public function mdlBorrarUsuario($tabla, $datos)
    {

        $stmt = Conexion::conectar()->prepare("DELETE FROM $tabla WHERE id = :id");

        $stmt->bindParam(":id", $datos, PDO::PARAM_INT);

        if ($stmt->execute()) {

            return "ok";
        } else {

            return "error";
        }

        $stmt->close();

        $stmt = null;
    }

    static public function mdlEditarUsuario($tabla, $datos)
    {

        $stmt = Conexion::conectar()->prepare("UPDATE $tabla SET id_empleado = :id_empleado, id_rol = :id_rol, usuario = :usuario, password = :password WHERE id = :id");

        $stmt->bindParam(":id_empleado", $datos["id_empleado"], PDO::PARAM_STR);
        $stmt->bindParam(":id_rol", $datos["id_rol"], PDO::PARAM_STR);
        $stmt->bindParam(":usuario", $datos["usuario"], PDO::PARAM_STR);
        $stmt->bindParam(":password", $datos["password"], PDO::PARAM_STR);

        $stmt->bindParam(":id", $datos["id"], PDO::PARAM_INT);

        if ($stmt->execute()) {

            return "ok";
        } else {

            return "error";
        }

        $stmt->close();
        $stmt = null;
    }
}
